package LOOPS;

public class DoWhileLoop01 {
    public static void main(String[] args) {
        System.out.println("------ DoWhileLoop01 is running -------");

        int i = 0;
        do {
            System.out.println("Iteration: " + i);
            i++;
        } while (i < 5);

        

        System.out.println("-------- DoWhileLoop01 ended ---------");
    } 
    


}
